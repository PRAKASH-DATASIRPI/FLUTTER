void main() {
  int num = 17;
  String word = "dart";
  double point = 12.0;
  bool positive = true;

  print("number is $num , word is $word,decimal is $point,value is $positive");

  dynamic value =
      "Hello Dart"; //can store implicitly any value during running the program.
  print(value);

  // Assigning value to geek1
  // variable without datatype
  final word1 = "prakash";

  // Printing variable geek1
  print(word1);

  // Assigning value to geek2
  // variable with datatype
  final String geek2 = "Geeks For Geeks Again!!";

  // Printing variable geek2
  print(geek2);

  // Assigning value to geek1
  // variable without datatype
  const geek = "Geeks For Geeks";

  // Printing variable geek1
  print(geek);

  // Assigning value to
  // geek2 variable with datatype
  const String geek3 = "Geeks For Geeks Again!!";

  // Printing variable geek2
  print(geek3);
}
